-- SP cộng điểm thủ công
CREATE OR REPLACE PROCEDURE SP_CONG_DIEM (
    p_makh    IN VARCHAR2,
    p_so_diem IN NUMBER,
    p_ly_do   IN NVARCHAR2,
    p_mahd    IN VARCHAR2 DEFAULT NULL
) AS
BEGIN
    UPDATE KHACH_HANG SET DIEMTICHLUY = DIEMTICHLUY + p_so_diem WHERE MAKH = p_makh;
    INSERT INTO LICH_SU_DIEM (MALSD, MAKH, LOAI, SO_DIEM, LY_DO, MAHD)
    VALUES (NULL, p_makh, N'Cong', p_so_diem, p_ly_do, p_mahd);
    COMMIT;
EXCEPTION WHEN OTHERS THEN ROLLBACK; RAISE;
END SP_CONG_DIEM;
/

-- SP đổi điểm lấy voucher (có thể tạo voucher mới hoặc lấy từ bảng KHUYEN_MAI)
CREATE OR REPLACE PROCEDURE SP_DOI_DIEM_LAY_VOUCHER (
    p_makh       IN VARCHAR2,
    p_diem_doi   IN NUMBER,
    p_makm_out   OUT VARCHAR2
) AS
    v_diem_hien_tai NUMBER;
    v_ma_km VARCHAR2(10);
BEGIN
    SELECT DIEMTICHLUY INTO v_diem_hien_tai FROM KHACH_HANG WHERE MAKH = p_makh;
    IF v_diem_hien_tai < p_diem_doi THEN
        RAISE_APPLICATION_ERROR(-20092, 'Không đủ điểm để đổi!');
    END IF;
    -- Tìm khuyến mãi có loại DoiDiem và giá trị điểm tương ứng
    SELECT MAKM INTO v_ma_km
    FROM KHUYEN_MAI
    WHERE LOAI = N'DoiDiem' AND GIA_TRI <= p_diem_doi AND IS_ACTIVE = 1
      AND NGAY_BD <= SYSDATE AND NGAY_KT >= SYSDATE
      AND (SO_LUONG IS NULL OR DA_DUNG < SO_LUONG)
      AND ROWNUM = 1;
    IF v_ma_km IS NULL THEN
        RAISE_APPLICATION_ERROR(-20093, 'Không có khuyến mãi phù hợp!');
    END IF;
    -- Trừ điểm
    UPDATE KHACH_HANG SET DIEMTICHLUY = DIEMTICHLUY - p_diem_doi WHERE MAKH = p_makh;
    INSERT INTO LICH_SU_DIEM (MALSD, MAKH, LOAI, SO_DIEM, LY_DO)
    VALUES (NULL, p_makh, N'Tru', p_diem_doi, N'Đổi điểm lấy voucher');
    -- Ghi nhận voucher vào bảng KH_KHUYEN_MAI (sẽ được áp dụng sau)
    INSERT INTO KH_KHUYEN_MAI (MAKH, MAKM) VALUES (p_makh, v_ma_km);
    p_makm_out := v_ma_km;
    COMMIT;
EXCEPTION WHEN OTHERS THEN ROLLBACK; RAISE;
END SP_DOI_DIEM_LAY_VOUCHER;
/
