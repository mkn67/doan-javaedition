-- Bảng Lịch sử điểm thưởng
CREATE TABLE LICH_SU_DIEM (
    MALSD     VARCHAR2(10)  PRIMARY KEY,
    MAKH      VARCHAR2(10)  NOT NULL,
    LOAI      NVARCHAR2(20) NOT NULL,   -- Cong | Tru | Quy_doi | Hoan
    SO_DIEM   NUMBER        NOT NULL,
    LY_DO     NVARCHAR2(200),
    MAHD      VARCHAR2(10)  NULL,
    THOI_GIAN TIMESTAMP     DEFAULT SYSTIMESTAMP,
    CONSTRAINT FK_LSD_KH FOREIGN KEY (MAKH) REFERENCES KHACH_HANG(MAKH),
    CONSTRAINT FK_LSD_HD FOREIGN KEY (MAHD) REFERENCES HOA_DON(MAHD),
    CONSTRAINT CK_LSD_LOAI CHECK (LOAI IN (N'Cong', N'Tru', N'Quy_doi', N'Hoan'))
);
-- Lịch sử thay đổi giá sản phẩm
CREATE TABLE LICH_SU_GIA (
    MALSG          VARCHAR2(10)  PRIMARY KEY,
    MASP           VARCHAR2(10)  NOT NULL,
    GIA_CU         NUMBER(15,2)  NOT NULL,
    GIA_MOI        NUMBER(15,2)  NOT NULL,
    NGAY_AP_DUNG   TIMESTAMP     DEFAULT SYSTIMESTAMP,
    NGUOI_CAP_NHAT VARCHAR2(50),
    LY_DO          NVARCHAR2(200),
    CONSTRAINT FK_LSG_SP FOREIGN KEY (MASP) REFERENCES SAN_PHAM(MASP)
);
CREATE SEQUENCE SEQ_LICH_SU_GIA START WITH 1 INCREMENT BY 1 CACHE 10 NOCYCLE;
CREATE OR REPLACE TRIGGER TRG_GEN_MALSG
BEFORE INSERT ON LICH_SU_GIA FOR EACH ROW
BEGIN IF :NEW.MALSG IS NULL THEN :NEW.MALSG := 'LG' || LPAD(SEQ_LICH_SU_GIA.NEXTVAL,6,'0'); END IF; END;
/

-- 14. Lịch sử giá — tự ghi khi giá sản phẩm thay đổi
CREATE OR REPLACE TRIGGER TRG_LICH_SU_GIA
AFTER UPDATE OF GIABAN ON SAN_PHAM
FOR EACH ROW
WHEN (OLD.GIABAN != NEW.GIABAN)
BEGIN
    INSERT INTO LICH_SU_GIA(MALSG, MASP, GIA_CU, GIA_MOI, NGAY_AP_DUNG, NGUOI_CAP_NHAT)
    VALUES (NULL, :NEW.MASP, :OLD.GIABAN, :NEW.GIABAN, SYSTIMESTAMP, USER);
END;
/
CREATE SEQUENCE SEQ_LICH_SU_DIEM START WITH 1 INCREMENT BY 1 CACHE 50 NOCYCLE;

CREATE OR REPLACE TRIGGER TRG_GEN_MALSD
BEFORE INSERT ON LICH_SU_DIEM
FOR EACH ROW
BEGIN
    IF :NEW.MALSD IS NULL THEN
        :NEW.MALSD := 'LS' || LPAD(SEQ_LICH_SU_DIEM.NEXTVAL, 6, '0');
    END IF;
END;
/

