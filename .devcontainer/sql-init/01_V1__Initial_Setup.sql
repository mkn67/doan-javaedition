    NOI_CAP              NVARCHAR2(200),
    KINH_NGHIEM_NAM      NUMBER(2)     DEFAULT 0,
    GHI_CHU              NVARCHAR2(500),
    CONSTRAINT FK_CTKT_NS FOREIGN KEY (MANS) REFERENCES NHAN_SU(MANS)
);

-- Lịch làm việc bác sĩ/kỹ thuật viên
CREATE TABLE LICH_LAM_VIEC (
    MALLV        VARCHAR2(10)  PRIMARY KEY,
    MANS         VARCHAR2(10)  NOT NULL,
    NGAY_LAM     DATE          NOT NULL,
    GIO_BAT_DAU  NUMBER(4,2)   NOT NULL,   -- 8.0 = 8:00, 13.5 = 13:30
    GIO_KET_THUC NUMBER(4,2)   NOT NULL,
    IS_NGHI      NUMBER(1)     DEFAULT 0,
    CONSTRAINT FK_LLV_NS  FOREIGN KEY (MANS) REFERENCES NHAN_SU(MANS),
