CREATE OR REPLACE PROCEDURE SP_TAO_HOA_DON (
    p_makh        IN VARCHAR2,
    p_mans        IN VARCHAR2,
    p_mahoso      IN VARCHAR2,
    p_madon       IN VARCHAR2,
    p_json_sp     IN CLOB, -- Dạng: '[{"malo":"L01","sl":2,"gia":150000}]'
    p_json_dv     IN CLOB, -- Dạng: '[{"madv":"DV01","sl":1,"gia":500000}]'
    p_mahd_out    OUT VARCHAR2
) AS
    v_mahd VARCHAR2(20);
BEGIN
    -- 1. Tạo vỏ Hóa Đơn (TONGTIEN ban đầu = 0, Trigger sẽ tự cộng dồn)
    INSERT INTO HOA_DON(MAHD, MAKH, MANS, MAHOSO, MADON, NGAYLAP, TONGTIEN, TRANGTHAI, IS_DELETED)
    VALUES (NULL, p_makh, p_mans, p_mahoso, p_madon, SYSTIMESTAMP, 0, N'Chưa thanh toán', 0)
    RETURNING MAHD INTO v_mahd;
    -- 2. Insert Chi tiết Sản phẩm (dùng JSON_TABLE bung chuỗi JSON)
    IF p_json_sp IS NOT NULL THEN
        INSERT INTO CT_HOA_DON (MAHD, MALO, SOLUONG, DON_GIA)
        SELECT v_mahd, j.malo, j.sl, j.gia
        FROM JSON_TABLE(p_json_sp, '$[*]'
            COLUMNS (
                malo VARCHAR2(20) PATH '$.malo',
                sl   NUMBER       PATH '$.sl',
                gia  NUMBER       PATH '$.gia'
            )
        ) j;
    END IF;
    -- 3. Insert Chi tiết Dịch vụ
    IF p_json_dv IS NOT NULL THEN
        INSERT INTO CT_HOA_DON_DV (MAHD, MADV, SOLUONG, DONGIA)
        SELECT v_mahd, j.madv, j.sl, j.gia
        FROM JSON_TABLE(p_json_dv, '$[*]'
            COLUMNS (
                madv VARCHAR2(20) PATH '$.madv',
                sl   NUMBER       PATH '$.sl',
                gia  NUMBER       PATH '$.gia'
            )
        ) j;
    END IF;

    p_mahd_out := v_mahd;
    COMMIT;
EXCEPTION 
    WHEN OTHERS THEN 
        ROLLBACK; 
        RAISE;
END SP_TAO_HOA_DON;
