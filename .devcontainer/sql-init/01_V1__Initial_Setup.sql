CREATE OR REPLACE TRIGGER TRG_GEN_MAHD
BEFORE INSERT ON HOA_DON FOR EACH ROW
BEGIN IF :NEW.MAHD IS NULL THEN :NEW.MAHD := 'HD' || LPAD(SEQ_HOA_DON.NEXTVAL,6,'0'); END IF; END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MATT
BEFORE INSERT ON THANH_TOAN FOR EACH ROW
BEGIN IF :NEW.MATT IS NULL THEN :NEW.MATT := 'TT' || LPAD(SEQ_THANH_TOAN.NEXTVAL,6,'0'); END IF; END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MALO
BEFORE INSERT ON LO_HANG FOR EACH ROW
BEGIN IF :NEW.MALO IS NULL THEN :NEW.MALO := 'LO' || LPAD(SEQ_LO_HANG.NEXTVAL,6,'0'); END IF; END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MAPN
BEFORE INSERT ON PHIEU_NHAP FOR EACH ROW
BEGIN IF :NEW.MAPN IS NULL THEN :NEW.MAPN := 'PN' || LPAD(SEQ_PHIEU_NHAP.NEXTVAL,6,'0'); END IF; END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MAPX
BEFORE INSERT ON PHIEU_XUAT FOR EACH ROW
BEGIN IF :NEW.MAPX IS NULL THEN :NEW.MAPX := 'PX' || LPAD(SEQ_PHIEU_XUAT.NEXTVAL,6,'0'); END IF; END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MALH
BEFORE INSERT ON LICH_HEN FOR EACH ROW
BEGIN IF :NEW.MALH IS NULL THEN :NEW.MALH := 'LH' || LPAD(SEQ_LICH_HEN.NEXTVAL,6,'0'); END IF; END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MAHOSO
BEFORE INSERT ON HO_SO_THI_LUC FOR EACH ROW
BEGIN IF :NEW.MAHOSO IS NULL THEN :NEW.MAHOSO := 'HS' || LPAD(SEQ_HO_SO.NEXTVAL,6,'0'); END IF; END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MALLV
BEFORE INSERT ON LICH_LAM_VIEC FOR EACH ROW
BEGIN IF :NEW.MALLV IS NULL THEN :NEW.MALLV := 'LV' || LPAD(SEQ_LICH_LAM_VIEC.NEXTVAL,6,'0'); END IF; END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MADG
BEFORE INSERT ON DANH_GIA FOR EACH ROW
BEGIN IF :NEW.MADG IS NULL THEN :NEW.MADG := 'DG' || LPAD(SEQ_DANH_GIA.NEXTVAL,6,'0'); END IF; END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MAHC
BEFORE INSERT ON HANG_CHO FOR EACH ROW
BEGIN
    IF :NEW.MAHC IS NULL THEN :NEW.MAHC := 'HC' || LPAD(SEQ_HANG_CHO.NEXTVAL,6,'0'); END IF;
    IF :NEW.SO_THU_TU IS NULL THEN
        SELECT NVL(MAX(SO_THU_TU),0)+1 INTO :NEW.SO_THU_TU
        FROM HANG_CHO WHERE TRUNC(GIO_DANG_KY) = TRUNC(SYSDATE);
    END IF;
END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MAXL
BEFORE INSERT ON XU_LY_KINH FOR EACH ROW
BEGIN IF :NEW.MAXL IS NULL THEN :NEW.MAXL := 'XL' || LPAD(SEQ_XU_LY_KINH.NEXTVAL,6,'0'); END IF; END;
/
CREATE OR REPLACE TRIGGER TRG_GEN_MAGD
BEFORE INSERT ON GIAO_DICH_NCC FOR EACH ROW
BEGIN IF :NEW.MAGD IS NULL THEN :NEW.MAGD := 'GD' || LPAD(SEQ_GIAO_DICH_NCC.NEXTVAL,6,'0'); END IF; END;
/

