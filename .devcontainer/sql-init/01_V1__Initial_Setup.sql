-- SP 6: Đặt lịch hẹn
CREATE OR REPLACE PROCEDURE SP_DAT_LICH_HEN (
    p_makh IN VARCHAR2, p_mans IN VARCHAR2, p_magoi IN VARCHAR2,
    p_ngayhen IN DATE, p_giohen IN TIMESTAMP, p_trieuchung IN JSON,
    p_malh_out OUT VARCHAR2
) AS
    v_ca_lam NUMBER; v_trung NUMBER;
