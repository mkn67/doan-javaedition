-- 1. Nhân Sự — tuổi >= 18, chặn trùng MATK
CREATE OR REPLACE TRIGGER TRG_VALIDATE_NHAN_SU
BEFORE INSERT OR UPDATE ON NHAN_SU
FOR EACH ROW
DECLARE
    v_tuoi  NUMBER;
    v_count NUMBER;
BEGIN
    v_tuoi := FLOOR(MONTHS_BETWEEN(SYSDATE, :NEW.NGAYSINH) / 12);
    IF v_tuoi < 18 THEN
        RAISE_APPLICATION_ERROR(-20010, 'LỖI: Nhân viên [' || :NEW.HOTEN || '] chưa đủ 18 tuổi!');
    END IF;
    IF INSERTING AND :NEW.MATK IS NOT NULL THEN
        SELECT COUNT(*) INTO v_count FROM NHAN_SU WHERE MATK = :NEW.MATK;
        IF v_count > 0 THEN
            RAISE_APPLICATION_ERROR(-20009, 'LỖI: Tài khoản đã được gán cho nhân sự khác!');
        END IF;
    END IF;
END;
/

-- 2. Khách Hàng — validate SĐT, chặn xóa cứng
CREATE OR REPLACE TRIGGER TRG_VALIDATE_KHACH_HANG
BEFORE INSERT OR UPDATE OR DELETE ON KHACH_HANG
FOR EACH ROW
DECLARE v_count NUMBER;
BEGIN
    IF INSERTING OR UPDATING THEN
        IF NOT REGEXP_LIKE(:NEW.SDT, '^[0-9]{9,11}$') THEN
            RAISE_APPLICATION_ERROR(-20013, 'LỖI: SĐT không hợp lệ (9-11 chữ số)!');
        END IF;
    END IF;
    IF DELETING THEN
        SELECT COUNT(*) INTO v_count FROM LICH_HEN WHERE MAKH = :OLD.MAKH AND TRANGTHAI = N'Mới';
        IF v_count > 0 THEN
            RAISE_APPLICATION_ERROR(-20050, 'LỖI: KH đang có lịch hẹn chờ — dùng Soft Delete!');
        END IF;
    END IF;
END;
/

-- 3. Lịch Hẹn — kiểm tra bác sĩ có lịch làm việc + không trùng slot
CREATE OR REPLACE TRIGGER TRG_VALIDATE_LICH_HEN
FOR INSERT OR UPDATE ON LICH_HEN
COMPOUND TRIGGER
    TYPE t_mahd_list IS TABLE OF VARCHAR2(10);
    v_mahd_arr t_mahd_list := t_mahd_list();

    BEFORE EACH ROW IS
        v_ca_lam NUMBER;
        v_ton_kh NUMBER;
        v_ton_ns NUMBER;
    BEGIN
        SELECT COUNT(*) INTO v_ton_kh FROM KHACH_HANG WHERE MAKH = :NEW.MAKH AND IS_DELETED = 0;
        IF v_ton_kh = 0 THEN RAISE_APPLICATION_ERROR(-20032, 'LỖI: Khách hàng không tồn tại!'); END IF;

        SELECT COUNT(*) INTO v_ton_ns FROM NHAN_SU WHERE MANS = :NEW.MANS AND IS_DELETED = 0;
        IF v_ton_ns = 0 THEN RAISE_APPLICATION_ERROR(-20033, 'LỖI: Bác sĩ không tồn tại hoặc đã nghỉ!'); END IF;

        SELECT COUNT(*) INTO v_ca_lam FROM LICH_LAM_VIEC
        WHERE MANS = :NEW.MANS AND NGAY_LAM = TRUNC(:NEW.NGAYHEN) AND IS_NGHI = 0;
        IF v_ca_lam = 0 THEN RAISE_APPLICATION_ERROR(-20030, 'LỖI: Bác sĩ không có lịch làm việc ngày này!'); END IF;

        IF :NEW.GIO_HEN IS NOT NULL THEN
            -- Lưu mã lịch hẹn để sau kiểm tra trùng (xử lý sau)
            v_mahd_arr.EXTEND;
            v_mahd_arr(v_mahd_arr.LAST) := :NEW.MALH;
        END IF;
    END BEFORE EACH ROW;

    AFTER STATEMENT IS
        v_trung NUMBER;
    BEGIN
        FOR i IN 1 .. v_mahd_arr.COUNT LOOP
            -- Kiểm tra trùng slot
            SELECT COUNT(*) INTO v_trung
            FROM LICH_HEN
            WHERE MANS = (SELECT MANS FROM LICH_HEN WHERE MALH = v_mahd_arr(i))
              AND GIO_HEN = (SELECT GIO_HEN FROM LICH_HEN WHERE MALH = v_mahd_arr(i))
              AND TRANGTHAI != N'Đã hủy'
              AND MALH != v_mahd_arr(i);
            IF v_trung > 0 THEN
                RAISE_APPLICATION_ERROR(-20031, 'LỖI: Bác sĩ đã có lịch slot này!');
            END IF;
        END LOOP;
    END AFTER STATEMENT;
END TRG_VALIDATE_LICH_HEN;
/
