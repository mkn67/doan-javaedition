-- Chi tiết chuyên môn/chứng chỉ — áp dụng Bác sĩ + Kỹ thuật viên
-- 1 nhân sự chỉ có 1 dòng chi tiết kỹ thuật (PK = MANS)
CREATE TABLE CHI_TIET_KY_THUAT (
    MANS                 VARCHAR2(10)  PRIMARY KEY,
    CHUYEN_NGANH         NVARCHAR2(100),
    CHUNG_CHI_HANH_NGHE  NVARCHAR2(100),   -- Số chứng chỉ hành nghề
