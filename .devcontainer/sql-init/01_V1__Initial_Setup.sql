    IF INSERTING OR UPDATING THEN
        SELECT cv.TENCV INTO v_tencv FROM NHAN_SU ns JOIN CHUC_VU cv ON ns.MACV = cv.MACV WHERE ns.MANS = :NEW.MANS;
