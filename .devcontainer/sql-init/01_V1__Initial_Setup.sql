-- 9. Thanh Toán — FIX: Sử dụng Array trong Compound Trigger tránh Mutating Table
CREATE OR REPLACE TRIGGER TRG_THANH_TOAN
FOR INSERT OR UPDATE ON THANH_TOAN
COMPOUND TRIGGER
    TYPE t_mahd_list IS TABLE OF VARCHAR2(10);
    v_mahd_arr t_mahd_list := t_mahd_list();

    BEFORE EACH ROW IS
        v_tong_hd NUMBER;
    BEGIN
        SELECT TONGTIEN INTO v_tong_hd FROM HOA_DON WHERE MAHD = :NEW.MAHD;
        IF :NEW.SOTIEN <= 0 OR :NEW.SOTIEN > v_tong_hd THEN
            RAISE_APPLICATION_ERROR(-20018, 'LỖI: Số tiền thanh toán không hợp lệ!');
        END IF;
    END BEFORE EACH ROW;

    AFTER EACH ROW IS
    BEGIN
        IF :NEW.TRANGTHAI = N'Thành công' THEN
            v_mahd_arr.EXTEND;
            v_mahd_arr(v_mahd_arr.LAST) := :NEW.MAHD;
        END IF;
    END AFTER EACH ROW;

    AFTER STATEMENT IS
        v_tong_hd NUMBER;
        v_da_tt   NUMBER;
        v_makh    VARCHAR2(10);
    BEGIN
        FOR i IN 1 .. v_mahd_arr.COUNT LOOP
            SELECT NVL(SUM(SOTIEN),0) INTO v_da_tt
            FROM THANH_TOAN WHERE MAHD = v_mahd_arr(i) AND TRANGTHAI = N'Thành công';

            SELECT TONGTIEN, MAKH INTO v_tong_hd, v_makh
            FROM HOA_DON WHERE MAHD = v_mahd_arr(i);

            IF v_da_tt >= v_tong_hd THEN
                UPDATE HOA_DON SET TRANGTHAI = N'Đã thanh toán' WHERE MAHD = v_mahd_arr(i);
                -- Cộng điểm thưởng (1 điểm/100k)
                UPDATE KHACH_HANG
                SET DIEMTICHLUY = NVL(DIEMTICHLUY,0) + FLOOR(v_tong_hd / 100000)
                WHERE MAKH = v_makh;
                -- Ghi lịch sử điểm
                INSERT INTO LICH_SU_DIEM(MALSD, MAKH, LOAI, SO_DIEM, LY_DO, MAHD)
                VALUES (NULL, v_makh, N'Cong', FLOOR(v_tong_hd / 100000), N'Tích điểm từ hóa đơn', v_mahd_arr(i));
            END IF;
        END LOOP;
    END AFTER STATEMENT;
END TRG_THANH_TOAN;
/
