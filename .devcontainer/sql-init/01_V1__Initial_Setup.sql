-- SP 1: Lưu hồ sơ khám bệnh
CREATE OR REPLACE PROCEDURE SP_LUU_HOSO_KHAM_BENH (
    p_mahoso       IN OUT VARCHAR2,
    p_makh         IN VARCHAR2, p_mans IN VARCHAR2, p_ketluan IN NVARCHAR2,
    p_mat_trai_sph IN NUMBER, p_mat_trai_cyl IN NUMBER, p_mat_trai_ax IN NUMBER, p_docong_trai IN NUMBER DEFAULT NULL,
    p_mat_phai_sph IN NUMBER, p_mat_phai_cyl IN NUMBER, p_mat_phai_ax IN NUMBER, p_docong_phai IN NUMBER DEFAULT NULL,
