CREATE OR REPLACE TRIGGER TRG_GEN_MAHC
FOR INSERT ON HANG_CHO
COMPOUND TRIGGER
    v_max_stt NUMBER := 0;

    BEFORE STATEMENT IS
    BEGIN
        -- Lấy STT lớn nhất của ngày hôm nay một lần duy nhất để tránh ORA-04091
        SELECT NVL(MAX(SO_THU_TU), 0) INTO v_max_stt
        FROM HANG_CHO WHERE TRUNC(GIO_DANG_KY) = TRUNC(SYSDATE);
    END BEFORE STATEMENT;

    BEFORE EACH ROW IS
    BEGIN
        IF :NEW.MAHC IS NULL THEN 
            :NEW.MAHC := 'HC' || LPAD(SEQ_HANG_CHO.NEXTVAL,6,'0'); 
        END IF;
        
        IF :NEW.SO_THU_TU IS NULL THEN
            v_max_stt := v_max_stt + 1;
            :NEW.SO_THU_TU := v_max_stt;
        END IF;
    END BEFORE EACH ROW;
END TRG_GEN_MAHC;
