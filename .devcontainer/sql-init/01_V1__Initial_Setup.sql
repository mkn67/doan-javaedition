-- SP 3: Nhập kho lô hàng
CREATE OR REPLACE PROCEDURE SP_NHAP_KHO_LO_HANG (
    p_mapn IN OUT VARCHAR2, p_mancc IN VARCHAR2, p_mans IN VARCHAR2,
    p_malo IN OUT VARCHAR2, p_masp IN VARCHAR2,
    p_ngaysx IN DATE, p_ngayhethan IN DATE,
    p_soluongnhap IN NUMBER, p_gianhap IN NUMBER,
    p_tongtien_out OUT NUMBER
) AS
    v_existing NUMBER; v_tongtien NUMBER;
BEGIN
    IF p_mapn IS NULL THEN
        INSERT INTO PHIEU_NHAP(MAPN,MANCC,MANS,NGAYNHAP,TONGTIEN)
        VALUES (NULL,p_mancc,p_mans,SYSTIMESTAMP,0) RETURNING MAPN INTO p_mapn;
    ELSE
        SELECT COUNT(*) INTO v_existing FROM PHIEU_NHAP WHERE MAPN = p_mapn;
        IF v_existing = 0 THEN
            INSERT INTO PHIEU_NHAP(MAPN,MANCC,MANS,NGAYNHAP,TONGTIEN) VALUES (p_mapn,p_mancc,p_mans,SYSTIMESTAMP,0);
        END IF;
    END IF;
    IF p_ngayhethan <= p_ngaysx  THEN RAISE_APPLICATION_ERROR(-20020,'LỖI: HSD phải sau NSX!'); END IF;
    IF p_ngayhethan <= SYSDATE   THEN RAISE_APPLICATION_ERROR(-20021,'LỖI: Không nhập hàng hết hạn!'); END IF;
    INSERT INTO LO_HANG(MALO,MASP,MAPN,NGAYSANXUAT,NGAYHETHAN,SOLUONGNHAP,GIANHAP)
    VALUES (p_malo,p_masp,p_mapn,p_ngaysx,p_ngayhethan,p_soluongnhap,p_gianhap) RETURNING MALO INTO p_malo;
    SELECT TONGTIEN INTO v_tongtien FROM PHIEU_NHAP WHERE MAPN = p_mapn;
    p_tongtien_out := v_tongtien;
    COMMIT;
EXCEPTION WHEN OTHERS THEN ROLLBACK; RAISE;
END SP_NHAP_KHO_LO_HANG;
/

-- SP 4: Cảnh báo hàng hết hạn
CREATE OR REPLACE PROCEDURE SP_CANH_BAO_HANG_HET_HAN (
    p_so_ngay IN NUMBER, c_result OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN c_result FOR
        SELECT l.MALO, s.MASP, s.TENSP, s.DONVITINH, l.NGAYHETHAN,
               ROUND(l.NGAYHETHAN - SYSDATE) AS SO_NGAY_CON_LAI, l.SOLUONGTON AS TON_KHO,
               CASE WHEN ROUND(l.NGAYHETHAN-SYSDATE)<=7  THEN N'Nguy hiểm'
                    WHEN ROUND(l.NGAYHETHAN-SYSDATE)<=30 THEN N'Cảnh báo'
                    ELSE N'Chú ý' END AS MUC_DO,
               ncc.TENNCC AS NHA_CUNG_CAP
        FROM   LO_HANG l
        JOIN   SAN_PHAM s  ON l.MASP  = s.MASP
        JOIN   PHIEU_NHAP pn ON l.MAPN = pn.MAPN
        JOIN   NHA_CUNG_CAP ncc ON pn.MANCC = ncc.MANCC
        WHERE  l.SOLUONGTON > 0 AND l.NGAYHETHAN > SYSDATE
          AND  (l.NGAYHETHAN - SYSDATE) <= p_so_ngay
        ORDER  BY l.NGAYHETHAN ASC;
END SP_CANH_BAO_HANG_HET_HAN;
/

-- SP 5: Thống kê doanh thu tháng
CREATE OR REPLACE PROCEDURE SP_THONG_KE_DOANH_THU_THANG (
    p_thang IN NUMBER, p_nam IN NUMBER, c_data OUT SYS_REFCURSOR
) AS
BEGIN
    OPEN c_data FOR
        SELECT TO_CHAR(NGAYTHANHTOAN,'DD/MM/YYYY') AS NGAY,
               COUNT(MATT) AS SO_LUONG_DON, SUM(SOTIEN) AS DOANH_THU_NGAY
        FROM   THANH_TOAN
        WHERE  TRANGTHAI = N'Thành công'
